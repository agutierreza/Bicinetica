package bicinetica.com.bicinetica.data;

public class Record {

    private int id;
    private String name;

    public Record() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id)  {
        this.id = id;
    }
}
